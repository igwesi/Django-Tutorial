#Integer
num = 10
print(num)

#Float
float_num = 10.5
print(float_num)

#String
str_num = "10"
print(str_num)

#Boolean
bool_num = True
print(bool_num)

#List
list_num = [1, 2, 3, 4, 5]
print(list_num)

#Dictionary
dict_num = {"username": "John", "age": 32}
print(dict_num)

#Tuple
tuple_num = (1, 2, 3, 4, 5)
print(tuple_num)

#Set
set_num = {1, 2, 3, 4, 5}
print(set_num)

#Complex
complex_num = 10 + 2j
print(complex_num)

#Type Casting

str_num_int = int(str_num)
print(str_num_int)

float_num_int = int(float_num)
print(float_num_int)

float_num_str = str(float_num)
print(float_num_str)



#Type Checking
print(type(num))
print(type(float_num))
print(type(str_num))
print(type(bool_num))
print(type(complex_num))
print(type(set_num))
print(type(dict_num))
print(type(list_num))
print(type(tuple_num))

